def build_qa_prompt(question: str, context: str, history: str = "", source: str = "telegram") -> str:
    template = WB_QA_PROMPT_TEMPLATE if source == "wb" else TG_QA_PROMPT_TEMPLATE
    return template.format(
        question=question, 
        context=context, 
        history=history
    )
