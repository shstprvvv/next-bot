def build_qa_prompt(question: str, context: str, history: str = "", source: str = "telegram") -> str:
    if source == "wb_chat":
        template = WB_CHAT_PROMPT_TEMPLATE
    elif source == "ozon_chat":
        template = OZON_CHAT_PROMPT_TEMPLATE
    elif source == "wb":
        template = WB_QUESTION_PROMPT_TEMPLATE
    elif source == "ozon_question":
        template = OZON_QUESTION_PROMPT_TEMPLATE
    elif source == "ozon_review":
        template = OZON_REVIEW_PROMPT_TEMPLATE
    else:
        template = TG_QA_PROMPT_TEMPLATE
        
    return template.format(
        question=question, 
        context=context, 
        history=history
    )
